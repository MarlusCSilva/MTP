package model;


public class Calculadora {
    public int num1;
    public int num2;
    public float n1;
    public float n2;
    
    public int soma()
    {
        return num1+num2;
    }
    public int multiplicar(){
        return num1*num2;
    }
    public int subtrair(){
        return num1-num2;
    }
    public float dividir(){
        return n1/n2;
    }
            
}
